package com.example.Springassignment2.task1;

public class Calculator {
     public int add(int firstNumb, int secondNumb){
         return firstNumb+secondNumb;
     }

     public int sub(int firstNumb, int secondNumb){
         return firstNumb-secondNumb;
     }

     public int mult(int firstNumb, int secondNumb){
         return firstNumb*secondNumb;
     }

     public int div(int firstNumb, int secondNumb){
         return (firstNumb/secondNumb);
     }
}
